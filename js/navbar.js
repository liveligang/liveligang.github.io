function lihat_menu() {
    document.getElementById("lihat-menu").style.display = "none";
    document.getElementById("tutup-menu").style.display = "block";
    document.getElementById("menu").style.display = "block";
}
function tutup_menu() {
    document.getElementById("lihat-menu").style.display = "block";
    document.getElementById("tutup-menu").style.display = "none";
    document.getElementById("menu").style.display = "none";
}